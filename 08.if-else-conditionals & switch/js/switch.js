const age = 19;
switch (age) {
    case 19:
        console.log('You are 19');
        break;
    
    case 22:
        console.log('You are 22');
        break;
    
    case 24:
        console.log('You are 24');
        break;

    default:
        console.log('You are unknown age');
        break;
}